#Operadores Logicos
print('\n#Operadores Logicos')

print(True or False)
print(7 != 3 and 2 > 3)

#Tabela verdade do AND
print(True and True)
print(True and False)
print(False and True)
print(False and False)

#Tabela verdade do OR
print(True or True)
print(True or False)
print(False or True)
print(False or False)

#Tabela verdade do XOR
print(True != True)
print(True != False)
print(False != True)
print(False != False)

#Tabela de Negação (unario)
print(not True)
print(not False)